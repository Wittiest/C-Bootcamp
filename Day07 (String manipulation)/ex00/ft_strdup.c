#include <stdlib.h>

char	*ft_strcpy(char *dest, char *src)
{
	int index;

	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char *str;

	str = (char *)malloc(sizeof(*src));
	ft_strcpy(str, src);
	return (str);
}
